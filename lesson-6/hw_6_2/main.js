// Перевести до великого регістру наступні стрінгові значення
// 'hello world', 'lorem ipsum', 'javascript is cool'

const string1 = "hello world";
const string1Upper = string1.toUpperCase();
console.log(string1, string1Upper);

const string2 = "lorem ipsum";
const string2Upper = string2.toUpperCase();
console.log(string2, string2Upper);

const string3 = "javascript is cool";
const string3Upper = string3.toUpperCase();
console.log(string3, string3Upper);
